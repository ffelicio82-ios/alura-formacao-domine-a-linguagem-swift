// Cálculo da temperatura

var fahrenheit: Double
var celsius : Double = 32

fahrenheit = celsius * 9 / 5 + 32
print("\(celsius)°C convertido para Farenheit é: \(fahrenheit)°F")

celsius = 40
fahrenheit = celsius * 9 / 5 + 32
print("\(celsius)°C convertido para Farenheit é: \(fahrenheit)°F")


